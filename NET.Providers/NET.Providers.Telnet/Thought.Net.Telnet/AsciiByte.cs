using System;

namespace Thought.Net.Telnet
{

	/// <summary>
	/// ASCII constants related to the Telnet protocol.
	/// </summary>
	public static class AsciiByte
	{

		/// <summary>
		///     Null character (ASCII 0)
		/// </summary>
		public const byte NUL = 0;


		/// <summary>
		///     Bell (ASCII 7)
		/// </summary>
		public const byte BEL = 7;


		/// <summary>
		///     Backspace (ASCII 8)
		/// </summary>
		public const byte BS = 8;


		/// <summary>
		///     Horizontal tab (ASCII 9)
		/// </summary>
		public const byte HT = 9;


		/// <summary>
		///     Line Feed (ASCII 10)
		/// </summary>
		public const byte LF = 10;


		/// <summary>
		///     Vertical Tab (ASCII 11)
		/// </summary>
		public const byte VT = 11;


		/// <summary>
		///     Form Feed (ASCII 12)
		/// </summary>
		public const byte FF = 12;


		/// <summary>
		///     Carriage Return (ASCII 13)
		/// </summary>
		public const byte CR = 13;
	}
}
